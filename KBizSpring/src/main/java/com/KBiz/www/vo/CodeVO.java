package com.KBiz.www.vo;

public class CodeVO {

	private String cdno;
	private int cdlv;
	private String highcd;
	private String cdname;
	private String use;
	public String getCdno() {
		return cdno;
	}
	public void setCdno(String cdno) {
		this.cdno = cdno;
	}
	public int getCdlv() {
		return cdlv;
	}
	public void setCdlv(int cdlv) {
		this.cdlv = cdlv;
	}
	public String getHighcd() {
		return highcd;
	}
	public void setHighcd(String highcd) {
		this.highcd = highcd;
	}
	public String getCdname() {
		return cdname;
	}
	public void setCdname(String cdname) {
		this.cdname = cdname;
	}
	public String getUse() {
		return use;
	}
	public void setUse(String use) {
		this.use = use;
	}
}

package com.KBiz.www.vo;

public class CodeVO {

	private String cdno;
	private int cdlv;
	private String highcd;
	private String cdnm;
	private String use;
	private String itemnm;
	private String itemcd;
	
	public String getCdno() {
		return cdno;
	}
	public String getCdnm() {
		return cdnm;
	}
	public void setCdnm(String cdnm) {
		this.cdnm = cdnm;
	}
	public void setCdno(String cdno) {
		this.cdno = cdno;
	}
	public int getCdlv() {
		return cdlv;
	}
	public void setCdlv(int cdlv) {
		this.cdlv = cdlv;
	}
	public String getHighcd() {
		return highcd;
	}
	public void setHighcd(String highcd) {
		this.highcd = highcd;
	}
	public String getUse() {
		return use;
	}
	public void setUse(String use) {
		this.use = use;
	}
	public String getItemnm() {
		return itemnm;
	}
	public void setItemnm(String itemnm) {
		this.itemnm = itemnm;
	}
	public String getItemcd() {
		return itemcd;
	}
	public void setItemcd(String itemcd) {
		this.itemcd = itemcd;
	}
	
	
}
